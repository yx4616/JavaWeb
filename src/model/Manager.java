package model;

public class Manager {
    private  int id;
    private int userId;
    private String title;
    private String contnt;
    private String titme;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContnt() {
        return contnt;
    }

    public void setContnt(String contnt) {
        this.contnt = contnt;
    }

    public String getTitme() {
        return titme;
    }

    public void setTitme(String titme) {
        this.titme = titme;
    }
}
